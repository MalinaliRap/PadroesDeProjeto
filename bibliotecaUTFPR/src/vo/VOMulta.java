package vo;

public class VOMulta {

	private String id;
	private float valorFixoDiario;
	private float valorTotal;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the valorFixoDiario
     */
    public float getValorFixoDiario() {
        return valorFixoDiario;
    }

    /**
     * @param valorFixoDiario the valorFixoDiario to set
     */
    public void setValorFixoDiario(float valorFixoDiario) {
        this.valorFixoDiario = valorFixoDiario;
    }

    /**
     * @return the valorTotal
     */
    public float getValorTotal() {
        return valorTotal;
    }

    /**
     * @param valorTotal the valorTotal to set
     */
    public void setValorTotal(float valorTotal) {
        this.valorTotal = valorTotal;
    }

}