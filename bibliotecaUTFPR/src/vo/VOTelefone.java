package vo;

public class VOTelefone {


	private String id;
	private String ddd;
	private String numero;
	private String indentificadorPais;


    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the ddd
     */
    public String getDdd() {
        return ddd;
    }

    /**
     * @param ddd the ddd to set
     */
    public void setDdd(String ddd) {
        this.ddd = ddd;
    }

    /**
     * @return the numero
     */
    public String getNumero() {
        return numero;
    }

    /**
     * @param numero the numero to set
     */
    public void setNumero(String numero) {
        this.numero = numero;
    }

    /**
     * @return the indentificadorPais
     */
    public String getIndentificadorPais() {
        return indentificadorPais;
    }

    /**
     * @param indentificadorPais the indentificadorPais to set
     */
    public void setIndentificadorPais(String indentificadorPais) {
        this.indentificadorPais = indentificadorPais;
    }

}